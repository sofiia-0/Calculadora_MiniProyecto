    double ans; // Variable para almacenar el resultado de la operación
    int calculo; // Variable que indica el tipo de operación aritmética a realizar

    public void operacion_aritmetica()
    {
        // Utilizar un switch para realizar diferentes operaciones según el valor de 'calculo'
        switch(calculo)
        {
            case 1: // Suma
                ans = num + Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;

            case 2: // Resta
                ans = num - Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;

            case 3: // Multiplicacion
                ans = num * Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;

            case 4: // Division
                ans = num / Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;
        }
    }

    
    
