    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Obtener la longitud del texto en el JTextField
        int longitud = jTextField1.getText().length();

        // Obtener el índice del último carácter en el JTextField
        int numero = jTextField1.getText().length() - 1;

        // Variable para almacenar el texto modificado
        String almacenado;

        // Verificar si hay al menos un carácter en el JTextField
        if (longitud > 0)
        {
            // Crear un StringBuilder con el contenido actual del JTextField
            StringBuilder atras = new StringBuilder(jTextField1.getText());

            // Eliminar el último carácter del StringBuilder
            atras.deleteCharAt(numero);

            // Convertir el StringBuilder de nuevo a String
            almacenado = atras.toString();

            // Establecer el nuevo texto en el JTextField
            jTextField1.setText(almacenado);
        }
        // Si no hay caracteres en el JTextField, no se realiza ninguna acción
    }//GEN-LAST:event_jButton1ActionPerformed

